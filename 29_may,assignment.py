class Person:
    def __init__(self, name, age):
        self.name = name
        self.age = age

    def show_details(self):
        print("Name:", self.name)
        print("Age:", self.age)


class Employee(Person):
    def __init__(self, name, age, salary):
        super().__init__(name, age)
        self.__salary = salary

    def get_salary(self):
        return self.__salary

    def show_details(self):
        super().show_details()
        print("Salary:", self.__salary)



class Manager(Employee):
    def __init__(self, name, age, salary):
        super().__init__(name, age, salary)

    def approve_leave(self, employee_name):
        print(f"Manager approved leave for {employee_name}")

    def view_employee_details(self, employee):
        print("\n Employee Details ")
        employee.show_details()

    def calculate_yearly_bonus(self):
        bonus = self.get_salary() * 0.20   # 20%
        print("Yearly Bonus:", bonus)


emp = Employee("Rahul", 25, 30000)
mgr = Manager("Amit", 40, 80000)

mgr.view_employee_details(emp)

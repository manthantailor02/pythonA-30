class BankAccount:
  def __init__(self,balance,name):
    self.name=name
    self.__balance=balance

  def deposit(self,amount):
    self.__balance=self.__balance+amount
    print('after deposit balance: ',self.__balance)

  def withdraw(self,amount):
    if amount<=self.__balance:
      self.__balance-=amount
      print('withdrawn: ', amount)
      print('balance after withdrawn: ',self.__balance)

  def get_balance(self):
    return self.__balance

  def calculate_interest(self):
    pass

class CurrentAccount(BankAccount):
  def calculate_interest(self):
    interest=self.get_balance()*0.02
    print('current account interest: ',interest)


class SavingsAccount(BankAccount):
  def calculate_interest(self):
    interest=self.get_balance()*0.05
    print('saving account interest: ',interest)

